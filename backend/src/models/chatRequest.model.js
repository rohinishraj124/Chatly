// models/chatRequest.model.js
import mongoose from "mongoose";

const chatRequestSchema = new mongoose.Schema(
  {
    sender: {
      type: String,
      required: true,
    },
    receiver: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      enum: ["pending", "accepted", "rejected"],
      default: "pending",
    },
  },
  { timestamps: true }
);

export const ChatRequest = mongoose.model("ChatRequest", chatRequestSchema);
